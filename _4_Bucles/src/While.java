public class While {
    public static void main(String[] args) {

        int i=1;
        int j = 10;

        while (i<=10){
            System.out.println(i);
            i++;
        }

        System.out.println("\n");

        while (j>=1){
            System.out.println(j);
            j--;
        }

    }
}