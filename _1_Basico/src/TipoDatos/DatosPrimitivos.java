package TipoDatos;

public class DatosPrimitivos {
    public static void main(String[] args) {

        byte numeroByte;
        System.out.println("numeroByte = " + Byte.MIN_VALUE);
        System.out.println("numeroByte = " + Byte.MAX_VALUE);

        short numeroShort;
        System.out.println("\nnumeroShort = " + Short.MIN_VALUE);
        System.out.println("numeroShort = " + Short.MAX_VALUE);

        int numeroInt;
        System.out.println("\nnumeroInt = " + Integer.MIN_VALUE);
        System.out.println("numeroInt = " + Integer.MAX_VALUE);

        long numeroLong;
        System.out.println("\nnumeroLong = " + Long.MIN_VALUE);
        System.out.println("numeroLong = " + Long.MAX_VALUE);

        float numeroFloat;
        System.out.println("\nnumeroFloat = " + Float.MIN_VALUE);
        System.out.println("numeroFloat = " + Float.MAX_VALUE);

        double numeroDouble;
        System.out.println("numeroDouble = " + Double.MIN_VALUE);
        System.out.println("numeroDouble = " + Double.MAX_VALUE);

        char letra = 'a';
        System.out.println("\nletra = " + letra);
        System.out.println("letra = " + Character.MIN_VALUE);
        System.out.println("letra = " + Character.MAX_VALUE);

        boolean decision;
        System.out.println("\ndecision = " + Boolean.TRUE);
        System.out.println("decision = " + Boolean.FALSE);

    }
}